public class Global {
    static byte a;
    static short b;
    static int c;
    static long d;
    static float e;
    static double f;
    static char g;
    static boolean h;
    public static void main(String[] args) {
        a = 21;
        b = 938;
        c = 74892;
        d = 9012345567l;
        e = 340.3f;
        f = 92.234;
        g = 'J';
        h = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }
}
