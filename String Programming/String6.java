//Print The Character by giving input Index ---> charAt(Index)
public class String6 {
    public static void main(String[] args) {
        String s1 = "Shaktiman";
        String s2 = "Perman";
        String s3 = "SuperMan";
        String s4 = "IronMan";

        System.out.println(s1.charAt(8));
        System.out.println(s2.charAt(3));
        System.out.println(s3.charAt(6));
        System.out.println(s4.charAt(7));
    }
}
