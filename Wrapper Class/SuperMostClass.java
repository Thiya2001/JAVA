public class SuperMostClass {
    public static void main(String[] args) {
        Object a = 10;
        System.out.println(a);

        Object b = 10.5;
        System.out.println(b);

        Object c = 'A';
        System.out.println(c);

        Object d = true;
        System.out.println(d);

        Object e = "Hello";
        System.out.println(e);
    }
}